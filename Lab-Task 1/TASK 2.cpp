#include<iostream>
using namespace std;

int main()

{
	while(1)
	{
		int n;
		cout<<"\n\nEnter a number: ";
		cin>>n;
		
		switch(n)
		{
			case 0:
				cout<<"\n\t ZERO";
				break;
			case 1:
				cout<<"\n\t ONE";
				break;
			case 2:
				cout<<"\n\t TWO";
				break;
			case 3:
				cout<<"\n\t THREE";
				break;
			case 4:
				cout<<"\n\t FOUR";
				break;
				
			case 5:
				cout<<"\n\t FIVE";
				break;
			case 6:
				cout<<"\n\t SIX";
				break;
			case 7:
				cout<<"\n\t SEVEN";
				break;
			case 8:
				cout<<"\n\t EIGHT";
				break;
			case 9:
				cout<<"\n\t NINE";
				break;
			
			default:
				 cout<<"\n\t INVALID INPUT";
				 exit(0);
			
		}
		
		
		
	}
	
	
	
	
	
	
	
	
}







